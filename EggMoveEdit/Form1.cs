        private void changeitem(object sender, EventArgs e)
        {
            if (!filling)
            {
                ComboBox[] box = cbtable();
                int count = 0;
                for (int i = 1; i < 21; i++)
                {
                    data[(int)(getspecies()), i] = getindex(box[i-1]);
                    if (getindex(box[i - 1]) > 0)
                    { count++; }
